# Sqrt_primorial.py
"""
This program makes a list of the square roots of primorial numbers in order
to make a growth rate model. This can directly be applied to the product 
of m(m+1) as the primorial(n) is the smallest number that can have an
omega value of n.

By Ken Clements, April 15, 2026
"""
from math import isqrt, log
from sympy import primorial
print("\n\n")
print(f"+-----------------------------------------------------------------------------------+")
print(f"|     n    |  2.25*ln(m)/ln(ln(m)  | m = Sqrt(Primorial(n)) (model for m in m(m+1)) |")
print(f"|----------+-----------------------+------------------------------------------------+")

for n in range (5, 25):
    g = isqrt(primorial(n))
    print(f"|   {n:3}    |         {int(2.25*log(g)/(log(log(g)))):3}           |    {g:40,}    |")
print(f"+-----------------------------------------------------------------------------------+\n\n")    

